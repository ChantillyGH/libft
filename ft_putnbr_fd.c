#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	unsigned int	nbr;

	if (n < 0)
	{
		ft_putchar_fd('-', fd);
		nbr = (unsigned int)(n * -1);
	}
	else
		nbr = (unsigned int)n;
	if (n > 9)
		ft_putchar_fd(nbr / 10, fd);
	ft_putchar_fd((char)nbr % 10 + 48, fd);
}
